
/**
 * op_add -  addition
 * @a: first number
 * @b: second number
 * Return: nothing
 *
 */
int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_add - sub
 * @a: first number
 * @b: second number
 * Return: substraction
 *
 */
int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - multiplication
 * @a: first number
 * @b: second number
 * Return: multiplication
 *
 */
int op_mul(int a, int b)
{
	return (a * b);
}
/**
 * op_div - division of two numbers
 * @a:first number
 * @b: second number
 * Return: division
 *
 */
int op_div(int a, int b)
{
	return (a / b);
}
/**
 * op_mod - modulo of two numbers
 * @a: first number
 * @b: second number
 * Return: modulo of two numbers
 */

int op_mod(int a, int b)
{
	return (a % b);
}
